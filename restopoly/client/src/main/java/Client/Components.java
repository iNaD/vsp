package Client;

public class Components {
    public String game;
    public String dice;
    public String board;
    public String bank;
    public String broker;
    public String decks;
    public String events;

    @Override
    public String toString() {
        return "Components{" +
                "game='" + game + '\'' +
                ", dice='" + dice + '\'' +
                ", board='" + board + '\'' +
                ", bank='" + bank + '\'' +
                ", broker='" + broker + '\'' +
                ", decks='" + decks + '\'' +
                ", events='" + events + '\'' +
                '}';
    }
}
